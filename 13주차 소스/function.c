void add(double *z, double x, double y)
{
	*z = x + y; 
}

void subtract(double *z, double x, double y)
{
	*z = x - y;  
}

void multiply(double *z, double x, double y)
{
	*z = x * y; 
}

void devide(double *z, double x, double y)
{
	*z = x / y;   
}
