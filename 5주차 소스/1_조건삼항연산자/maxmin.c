#include <stdio.h>

#define MAX(x, y) ((x < y) ? y : x)

int main(void) 
{
	int a = 2000;
	int b = 300;

	printf("두 수(%d, %d) 중에서 큰   수는 %d이다.\n", a, b, MAX(a, b));
	printf("두 수(%d, %d) 중에서 작은 수는 %d이다.\n", a, b, a < b ? a : b);
}
